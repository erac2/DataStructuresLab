public class Practice3 {

	public static int square(int num)
	{
		//return statement;
		return num * num;
	}
	
	public static void main(String[] args)
	{
	int result;
	
	//call the method
	//store the returned value to result
	result = square(10);
	
	System.out.println("Square of 10 is: " + result);
	}
	
}
